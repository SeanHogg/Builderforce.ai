/**
 * Fallback: Generate local analysis when API is unavailable
 */
function generateLocalAnalysis(chats: Chat[]): GroupedChatsAnalysis {
  // Simple grouping: group chats by their highest-confidence detected topic
  const categoryCounts: Record<string, Chat[]> = {};

  for (const chat of chats) {
    // Use the frontend utility to detect topics
    const detection = detectTopic(chat);

    if (!categoryCounts[detection.topic]) {
      categoryCounts[detection.topic] = [];
    }

    categoryCounts[detection.topic].push(chat);
  }

  const groups: ChatTopicGroup[] = Object.entries(categoryCounts).map(
    ([topic, chats]) => {
      return toChatGroup({
        chatIds: chats.map((c) => c.id),
        name: topic.toUpperCase(),
        description: `${chats.length} chats`,
        category: topic,
      });
    }
  );

  const topTopicsByChatCount = Object.entries(categoryCounts)
    .map(([topic, chats]) => ({ topic: topic, count: chats.length }))
    .sort((a, b) => b.count - a.count);

  const messageCountPerGroup: Record<string, number> = {};
  const totalMsgs = chats.reduce((acc, chat) => acc + chat.messages.length, 0);

  for (const group of groups) {
    const firstChat = chats.find((c) => c.id === group.chatIds[0]);
    if (firstChat) {
      messageCountPerGroup[group.id] = firstChat.messages.length;
    }
  }

  const customTopicNames: Record<string, string> = {};

  return {
    groups,
    topTopicsByChatCount,
    customTopicNames,
    messageCountPerGroup,
    totalMsgs,
  };
}

/**
 * Re-evaluate topics for a chat after new messages arrive
 */
export async function reevaluateChatTopic(
  chatId: string,
  newMessages: Array<{ text: string; sender: string }>
): Promise<string | null> {
  try {
    const response = await fetch('/api/topics/reevaluate', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({ chatId, newMessages }),
    });

    if (!response.ok) {
      throw new Error(`Failed to reevaluate topic: ${response.statusText}`);
    }

    const data = await response.json();
    return data.topic || null;
  } catch (error) {
    console.error('Reevaluating chat topic failed:', error);
    return null;
  }
}