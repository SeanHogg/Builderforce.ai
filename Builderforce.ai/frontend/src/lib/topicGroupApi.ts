/**
 * API functions for topic grouping feature
 *
 * Handles communication with backend endpoints for topic detection and grouping
 */

import type { Chat, GroupedChatsAnalysis, ChatTopicGroup } from '../types/chat';
import type { CustomTopicGroup, CustomGroupingState } from '../types/group';

/**
 * Convert detected chat groups to API-friendly format
 */
function toChatGroup(group: { chatIds: string[]; name: string; description?: string; category: string }): ChatTopicGroup {
  return {
    id: `group-${Date.now()}-${Math.random().toString(36).substr(2, 9)}`,
    name: group.name,
    description: group.description,
    chatIds: group.chatIds,
    autoGenerated: true,
  };
}

/**
 * Detect topics in a set of chats via API
 */
export async function detectTopics(chats: Chat[]): Promise<GroupedChatsAnalysis> {
  try {
    const response = await fetch('/api/topics/detect', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({ chats }),
    });

    if (!response.ok) {
      throw new Error(`API error: ${response.statusText}`);
    }

    const data = await response.json();
    return data;
  } catch (error) {
    console.error('Topic detection failed:', error);

    // Fallback to client-side detection
    return generateLocalAnalysis(chats);
  }
}

/**
 * Analyze chats and generate grouping analysis
 */
export async function analyzeChatTopics(
  chats: Chat[],
  options?: { minGroupSize?: number; defaultCategory?: string }
): Promise<GroupedChatsAnalysis> {
  return detectTopics(chats);
}

/**
 * Create a custom topic group via API
 */
export async function createCustomTopicGroup(
  group: Omit<CustomTopicGroup, 'createdAt' | 'lastModifiedAt'>
): Promise<CustomTopicGroup> {
  const sanitized = {
    ...group,
    createdAt: Date.now(),
    lastModifiedAt: Date.now(),
  };

  try {
    const response = await fetch('/api/groups', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify(sanitized),
    });

    if (!response.ok) {
      throw new Error(`Failed to create group: ${response.statusText}`);
    }

    return await response.json();
  } catch (error) {
    console.error('Creating custom group failed:', error);
    // Return client-side fallback
    return sanitized;
  }
}

/**
 * Update a custom topic group via API
 */
export async function updateCustomTopicGroup(
  groupId: string,
  updates: Partial<CustomTopicGroup>
): Promise<CustomTopicGroup> {
  const updated = {
    ...updates,
    lastModifiedAt: Date.now(),
  };

  try {
    const response = await fetch(`/api/groups/${groupId}`, {
      method: 'PATCH',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify(updated),
    });

    if (!response.ok) {
      throw new Error(`Failed to update group: ${response.statusText}`);
    }

    return await response.json();
  } catch (error) {
    console.error('Updating custom group failed:', error);
    return updated;
  }
}

