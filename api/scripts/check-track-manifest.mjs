#!/usr/bin/env node
/**
 * Isolation-track manifest validator + table generator (T9 · Platform · DB · CI).
 *
 * `.github/isolation-tracks.json` is the canonical, machine-readable source of
 * truth for the isolation tracks (consumed by check-track-scope.mjs). The human
 * "🧵 Isolation Tracks" table was hand-maintained alongside it, so the two could
 * silently drift (README gap register: "Isolation-Tracks table … hand-maintained").
 *
 * This script removes the hand-maintenance:
 *   1. VALIDATES the manifest is internally consistent — unique track ids, required
 *      fields, well-formed migration bands, and (critically) no two tracks claiming
 *      OVERLAPPING migration bands. An overlap is exactly the duplicate-number bug
 *      the band system exists to prevent, so it must fail at PR time, not in prod.
 *   2. GENERATES the canonical markdown table FROM the manifest into
 *      `.github/isolation-tracks.generated.md`. `--check` (CI mode) regenerates in
 *      memory and fails if the committed file drifts; default mode writes it.
 *
 * Usage:
 *   node scripts/check-track-manifest.mjs            # validate + (re)write the table
 *   node scripts/check-track-manifest.mjs --check    # validate + fail on table drift (CI)
 *
 * Exit 0 = valid + in sync; exit 1 = invalid manifest or (in --check) table drift.
 */
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = resolve(fileURLToPath(new URL('.', import.meta.url)));
const repoRoot = resolve(here, '../..');
const manifestFile = resolve(repoRoot, '.github/isolation-tracks.json');
const tableFile = resolve(repoRoot, '.github/isolation-tracks.generated.md');

const checkMode = process.argv.includes('--check');

if (!existsSync(manifestFile)) {
  console.error(`❌  Track manifest not found: ${manifestFile}`);
  process.exit(1);
}

let manifest;
try {
  manifest = JSON.parse(readFileSync(manifestFile, 'utf8'));
} catch (err) {
  console.error(`❌  Track manifest is not valid JSON: ${err.message}`);
  process.exit(1);
}

// ── validate ─────────────────────────────────────────────────────────────────
const errors = [];
const tracks = Array.isArray(manifest.tracks) ? manifest.tracks : null;
if (!tracks) errors.push('manifest.tracks must be an array');
if (!Array.isArray(manifest.sharedHubs) || manifest.sharedHubs.length === 0)
  errors.push('manifest.sharedHubs must be a non-empty array');

const seenIds = new Set();
const bands = []; // { id, band: [lo, hi] }
for (const t of tracks || []) {
  const where = `track ${t?.id ?? '(missing id)'}`;
  if (!t.id || typeof t.id !== 'string') errors.push(`${where}: missing/invalid id`);
  else if (seenIds.has(t.id)) errors.push(`duplicate track id "${t.id}"`);
  else seenIds.add(t.id);

  if (!t.name || typeof t.name !== 'string') errors.push(`${where}: missing/invalid name`);
  if (!Array.isArray(t.owns) || t.owns.length === 0) errors.push(`${where}: owns must be a non-empty array`);
  if (t.excludes != null && !Array.isArray(t.excludes)) errors.push(`${where}: excludes must be an array`);

  if (t.migrationBand != null) {
    const b = t.migrationBand;
    if (!Array.isArray(b) || b.length !== 2) {
      errors.push(`${where}: migrationBand must be [lo, hi|null] or null`);
    } else {
      const [lo, hi] = b;
      if (typeof lo !== 'number') errors.push(`${where}: migrationBand lower bound must be a number`);
      else if (hi != null && typeof hi !== 'number') errors.push(`${where}: migrationBand upper bound must be a number or null`);
      else if (hi != null && hi < lo) errors.push(`${where}: migrationBand upper bound ${hi} < lower bound ${lo}`);
      else bands.push({ id: t.id, lo, hi: hi == null ? Infinity : hi });
    }
  }
}

// No two reserved bands may overlap — that is the duplicate-number bug at its root.
for (let i = 0; i < bands.length; i++) {
  for (let j = i + 1; j < bands.length; j++) {
    const a = bands[i];
    const b = bands[j];
    if (a.lo <= b.hi && b.lo <= a.hi) {
      errors.push(
        `migration bands overlap: ${a.id} [${a.lo}-${a.hi === Infinity ? '∞' : a.hi}] ∩ ` +
          `${b.id} [${b.lo}-${b.hi === Infinity ? '∞' : b.hi}]`,
      );
    }
  }
}

if (errors.length > 0) {
  console.error(`❌  Track manifest invalid (${errors.length}):\n`);
  for (const e of errors) console.error(`   - ${e}`);
  process.exit(1);
}

// ── generate the canonical table ─────────────────────────────────────────────
const fmtBand = (b) => (b == null ? '—' : `${String(b[0]).padStart(4, '0')}–${b[1] == null ? '∞' : String(b[1]).padStart(4, '0')}`);
const cell = (s) => s.replace(/\|/g, '\\|');

const lines = [];
lines.push('<!-- GENERATED by api/scripts/check-track-manifest.mjs from .github/isolation-tracks.json — DO NOT EDIT BY HAND. -->');
lines.push('# 🧵 Isolation Tracks');
lines.push('');
lines.push('Canonical rendering of `.github/isolation-tracks.json`. Edit the JSON, then run');
lines.push('`node api/scripts/check-track-manifest.mjs` to regenerate this table.');
lines.push('');
lines.push('| Track | Owns (primary paths) | Mig band |');
lines.push('|---|---|---|');
for (const t of tracks) {
  const id = `**${t.id} · ${t.name}**${t.steward ? ' _(steward)_' : ''}`;
  const owns = t.owns.map((g) => `\`${g}\``).join(' · ');
  lines.push(`| ${cell(id)} | ${cell(owns)} | ${fmtBand(t.migrationBand)} |`);
}
lines.push('');
lines.push('**Shared hubs** (append-only, every track may edit): ' + manifest.sharedHubs.map((h) => `\`${h}\``).join(', ') + '.');
lines.push('');
const generated = lines.join('\n') + '\n';

if (checkMode) {
  const current = existsSync(tableFile) ? readFileSync(tableFile, 'utf8') : '';
  if (current !== generated) {
    console.error(
      `❌  ${tableFile} is out of sync with the manifest.\n` +
        '   Run `node api/scripts/check-track-manifest.mjs` and commit the result.',
    );
    process.exit(1);
  }
  console.log(`✅  Track manifest valid (${tracks.length} tracks) and generated table in sync.`);
} else {
  writeFileSync(tableFile, generated);
  console.log(`✅  Track manifest valid (${tracks.length} tracks) — wrote ${tableFile}.`);
}
